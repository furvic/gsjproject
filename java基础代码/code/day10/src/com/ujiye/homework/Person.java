package com.ujiye.homework;

public abstract class Person {
    private String name;
    private String no;

    public abstract void messagePrint();
    public Person() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }
}
